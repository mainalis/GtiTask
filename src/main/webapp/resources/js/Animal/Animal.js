'use strict';

class Animal {


    constructor(scientificName, color, birthDate, weight, givenName, breed, sex, height, hasHorns) {

        this.scientificName = scientificName;
        this.color = color;
        this.birthDate = birthDate;
        this.weight = weight;
        this.givenName = givenName;
        this.breed = breed;
        this.sex = sex;
        this.height = height;
        this.hasHorns = hasHorns;

    }

}